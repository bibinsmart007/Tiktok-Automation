/**
 * Get a topic based on rotation index (day of year)
 */
export function getTopicForDay(dayOfYear: number): Topic {
    const index = dayOfYear % TOPIC_DATABASE.length;
    return TOPIC_DATABASE[index];
}

/**
 * Get a random topic
 */
export function getRandomTopic(): Topic {
    const index = Math.floor(Math.random() * TOPIC_DATABASE.length);
    return TOPIC_DATABASE[index];
}

/**
 * Get topics filtered by niche
 */
export function getTopicsByNiche(niche: string): Topic[] {
    return TOPIC_DATABASE.filter((t) => t.niche === niche);
}
